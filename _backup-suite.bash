        cur="$2"
    else
        cur="${COMP_WORDS[COMP_CWORD]}"
    fi
    prev="$3"
    cmd=""
    opts=""

    for i in "${COMP_WORDS[@]:0:COMP_CWORD}"
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="backup__suite"
                ;;
            backup__suite,add)
                cmd="backup__suite__add"
                ;;
            backup__suite,cleanup)
